<h2>What is the purpose of this plugin?</h2>

This free plugin can be used to easily export your photos from NextGen, Envira, and FooGallery Wordpress gallery plugins for export to your computer.<br /><br />

<h2>How do I get started?</h2>

The process could not be simpler!<br />

<ol>
  <li>Visit the NextGen, FooGallery, or Envira tabs above for the corresponding plugin you are current using and a summary of the galleries currently configured for that plugin will be displayed.</li>
  <li>Click the Export button and wait for the system to create a ZIP file containing all your galleries and original sized photos organized in to corresponding subfolders.</li>
  <li>Click the provided URL to download the ZIP file from your server to your computer.</li>
  <li>After successfully downloading the file, you may optionally choose to delete the generated ZIP files from the History tab so that it doesn't take up extra space on your server.<br /><br /></li>
</ol>

<h2>Will this tool modify my existing Wordpress site?</h2>

This plugin will NOT make any changes to your existing Wordpress site and/or any of the galleries you have created with these other plugins. It is only a <b>read only utility</b> for exporting your current galleries and photos to your computer. It only creates a ZIP file containing your galleries and their photos (organized in to subfolders) so that you can download it to you computer. The ZIP file can then be deleted from your server after you've downloaded the file.<br /><br />

<h2>I have thousands of photos in my NextGen, FooGallery or Envira plugin. Can I still use this plugin?</h2>

We've tested this tool with thousands of large sized photos totalling 5GB+ of data on a typical Wordpress hosting server and its worked fine. However, its possible your Wordpress server is not powerful enough or have enough resources or storage space and the ZIP archive can not be created. In this case, you can alway export your galleries in smaller segments (by selecting a few galleries at a time) instead of attempting to create a full archive.<br /><br />

